class MessageDecorator < Draper::Base
  decorates :message

  def status
    read ? "read" : "unread"
  end
  
  def excerpt
    content[0..60]
  end
  
  def addressed_to_context
    sender_id == h.current_user.id ? "From You to #{recipient.name}".html_safe : "From #{sender.name} to You"
  end
  
